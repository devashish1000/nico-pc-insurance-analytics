with premium_match as (
  select distinct on (f.premium_key)
    f.premium_key, s.source_txn_id, s.source_updated_at, s.batch_id
  from public.fact_premium f
  join public.dim_policy p on p.policy_key = f.policy_key
  join staging.raw_premium_txn s
    on s.policy_number = p.policy_number
   and to_char(s.txn_date, 'YYYYMMDD')::integer = f.txn_date_key
   and s.written_premium is not distinct from f.written_premium
  order by f.premium_key, s.source_updated_at desc, s.landing_id desc
)
update public.fact_premium f
set source_txn_id = m.source_txn_id,
    source_updated_at = m.source_updated_at,
    load_batch_id = m.batch_id,
    loaded_at = coalesce(f.loaded_at, clock_timestamp()),
    calculation_as_of_date = coalesce(f.calculation_as_of_date, current_date)
from premium_match m
where f.premium_key = m.premium_key;

with claim_match as (
  select distinct on (f.loss_key)
    f.loss_key, s.source_txn_id, s.source_updated_at, s.batch_id
  from public.fact_loss f
  join staging.raw_claim_txn s
    on s.claim_number = f.claim_number
   and to_char(s.txn_date, 'YYYYMMDD')::integer = f.txn_date_key
  order by f.loss_key, s.source_updated_at desc, s.landing_id desc
)
update public.fact_loss f
set source_txn_id = m.source_txn_id,
    source_updated_at = m.source_updated_at,
    load_batch_id = m.batch_id,
    loaded_at = coalesce(f.loaded_at, clock_timestamp())
from claim_match m
where f.loss_key = m.loss_key;

do $$
begin
  if exists (
    select 1 from public.fact_premium
    where source_txn_id is null or source_updated_at is null or load_batch_id is null
       or loaded_at is null or calculation_as_of_date is null
  ) or exists (
    select 1 from public.fact_loss
    where source_txn_id is null or source_updated_at is null or load_batch_id is null
       or loaded_at is null
  ) then
    raise exception 'Warehouse v2 fact-key backfill did not resolve every existing fact';
  end if;

  if exists (
    select source_txn_id from public.fact_premium
    group by source_txn_id having count(*) > 1
  ) or exists (
    select source_txn_id from public.fact_loss
    group by source_txn_id having count(*) > 1
  ) then
    raise exception 'Warehouse v2 fact-key backfill produced duplicate source keys';
  end if;
end
$$;

