do $$
begin
  if not exists (
    select 1 from pg_constraint
    where conname = 'dim_policy_valid_range_check'
      and conrelid = 'public.dim_policy'::regclass
  ) then
    alter table public.dim_policy
      add constraint dim_policy_valid_range_check
      check (valid_to is null or valid_to > valid_from);
  end if;

  if exists (
    select 1
    from public.dim_policy a
    join public.dim_policy b
      on a.policy_number = b.policy_number
     and a.policy_key < b.policy_key
     and daterange(a.valid_from, coalesce(a.valid_to, 'infinity'::date), '[)') &&
         daterange(b.valid_from, coalesce(b.valid_to, 'infinity'::date), '[)')
  ) then
    raise exception 'Existing policy versions overlap; cannot enable SCD2 exclusion';
  end if;

  if not exists (
    select 1 from pg_constraint
    where conname = 'dim_policy_no_overlapping_versions'
      and conrelid = 'public.dim_policy'::regclass
  ) then
    alter table public.dim_policy
      add constraint dim_policy_no_overlapping_versions
      exclude using gist (
        policy_number with =,
        daterange(valid_from, coalesce(valid_to, 'infinity'::date), '[)') with &&
      );
  end if;
end
$$;

