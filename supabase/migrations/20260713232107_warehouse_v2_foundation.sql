revoke all on schema ops from public, anon, authenticated;
grant usage on schema ops to service_role;

create table if not exists ops.pipeline_batches (
  batch_id uuid primary key,
  run_id uuid references public.pipeline_runs(run_id),
  source_name text not null default 'all',
  mode text not null check (mode in ('initial', 'incremental', 'backfill', 'demo')),
  requested_from date,
  requested_to date,
  cutoff_at timestamptz,
  status text not null check (status in ('created', 'running', 'success', 'failed')),
  source_rows integer not null default 0,
  checksum text,
  created_at timestamptz not null default clock_timestamp(),
  finished_at timestamptz
);

create table if not exists ops.etl_watermarks (
  source_name text primary key check (source_name in ('premium', 'claim')),
  watermark_ts timestamptz not null,
  watermark_source_id text not null,
  last_successful_run_id uuid references public.pipeline_runs(run_id),
  updated_at timestamptz not null default clock_timestamp()
);

create table if not exists ops.pipeline_stage_runs (
  stage_run_id bigserial primary key,
  run_id uuid not null references public.pipeline_runs(run_id) on delete cascade,
  stage_order smallint not null,
  stage_name text not null,
  status text not null check (status in ('running', 'success', 'failed', 'skipped')),
  started_at timestamptz not null default clock_timestamp(),
  finished_at timestamptz,
  duration_ms integer,
  input_rows integer not null default 0,
  inserted_rows integer not null default 0,
  updated_rows integer not null default 0,
  unchanged_rows integer not null default 0,
  recalculated_rows integer not null default 0,
  rejected_rows integer not null default 0,
  error_code text,
  sanitized_error text,
  unique (run_id, stage_order)
);

create index if not exists pipeline_stage_runs_run_idx
  on ops.pipeline_stage_runs (run_id, stage_order);

create table if not exists ops.quarantine_records (
  quarantine_id uuid primary key default gen_random_uuid(),
  run_id uuid not null references public.pipeline_runs(run_id),
  batch_id uuid references ops.pipeline_batches(batch_id),
  source_name text not null check (source_name in ('premium', 'claim')),
  source_txn_id text not null,
  reason_code text not null,
  severity text not null check (severity in ('warning', 'critical')),
  payload jsonb not null default '{}'::jsonb,
  disposition text not null default 'pending'
    check (disposition in ('pending', 'replayed', 'discarded')),
  quarantined_at timestamptz not null default clock_timestamp(),
  resolved_at timestamptz,
  recovered_by_run_id uuid references public.pipeline_runs(run_id)
);

create index if not exists quarantine_records_run_idx
  on ops.quarantine_records (run_id, disposition);
create unique index if not exists quarantine_one_pending_source_idx
  on ops.quarantine_records (source_name, source_txn_id)
  where disposition = 'pending';

